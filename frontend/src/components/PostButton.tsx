/**
 * PostButton コンポーネント  
 * Issue#1: プロジェクトセットアップと基本レイアウト - 投稿ボタン（FAB）
 * 
 * 【目的】
 * - 新しい写真投稿機能へのメインエントリーポイント
 * - フローティングアクションボタン（FAB）パターンの実装
 * - 右下に固定配置される主要アクション
 * 
 * 【UIデザインパターン】
 * - Material Design のフローティングアクションボタンを参考
 * - 円形デザインで視覚的な重要度を強調
 * - ページの主要アクションを表現（写真投稿）
 * 
 * 【アクセシビリティ考慮】
 * - 十分なクリック領域（56px x 56px）
 * - 高コントラスト色（青背景に白文字）
 * - ホバー時の視覚的フィードバック
 * 
 * 【将来の拡張予定】
 * - クリック時に写真投稿モーダル表示
 * - ドラッグ&ドロップ対応
 * - 投稿進行状況の表示
 * 
 * 【TDD実装状況】
 * Green段階: テストを通すための最小実装（現在の状態）
 */
function PostButton() {
  return (
    <button 
      className={
        // === 背景とカラースキーム ===
        "bg-blue-500 " +              // 背景色: 青色（ブランドカラー、信頼感）
        "hover:bg-blue-600 " +        // ホバー時: より濃い青色（インタラクションフィードバック）
        "text-white " +               // テキスト色: 白色（背景との高コントラスト）
        
        // === 形状とサイズ ===
        "rounded-full " +             // 角の丸み: 完全な円形（50% border-radius）
        "w-14 h-14 " +                // サイズ: 56px x 56px（Material Designガイドライン準拠）
        
        // === 影とエレベーション ===
        "shadow-lg " +                // ドロップシャドウ: 大きめの影でフローティング感を演出
        
        // === レイアウト（Flexbox） ===
        "flex " +                     // Flexboxコンテナ: 中央揃えのために使用
        "items-center " +             // 垂直中央揃え: 子要素（+記号）を垂直方向の中央に配置
        "justify-center " +           // 水平中央揃え: 子要素（+記号）を水平方向の中央に配置
        
        // === タイポグラフィ ===
        "text-2xl " +                 // フォントサイズ: 24px（視認性重視）
        "font-bold " +                // フォント太さ: 太字（重要性を強調）
        
        // === アニメーション ===
        "transition-colors"           // トランジション: 色変化をスムーズに（0.15s duration）
      }
      
      // === アクセシビリティ属性 ===
      aria-label="新しい写真を投稿する"     // スクリーンリーダー用の説明
      title="写真を投稿"                   // ツールチップ表示用
      
      // TODO: Issue#3で投稿モーダル表示機能を実装
      // onClick={handlePostClick}
    >
      {/* 
        アイコン: プラス記号（+）
        - 国際的に「追加」「新規作成」を意味する記号
        - シンプルで言語に依存しない
        - フォントベース（軽量、スケーラブル）
      */}
      +
    </button>
  );
}

// ES6 モジュールとしてエクスポート
// App.tsx からインポートしてフローティング要素として配置
